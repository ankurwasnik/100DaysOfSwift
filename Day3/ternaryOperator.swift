// Ternary operator
//syntax : condition ? what if true : what if false
let alexMoney = 10000
let bobMoney = 12000

print( alexMoney == bobMoney ? "Alex & Bob contributes equally!" : "Alex needs to contribute more")

//Another example
let card1 = 10
let card2 = 20
print( card1 == card2 ? "Cards are same" :  "Cards are different" )